package com.misyshealthcare.connect.doc.ccd;

import java.util.ArrayList;
import java.util.List;

import com.misyshealthcare.connect.base.clinicaldata.Immunization;
import com.misyshealthcare.connect.base.clinicaldata.LabResult;
import com.misyshealthcare.connect.base.demographicdata.Insurance;

/**
 * The data container for Referral Summary.
 *
 * @author Wenzhi Li
 * @version 2.0, Oct 14, 2006
 */
public class ReferralSummaryData extends XDSMSData {

    /**Referral reason, required by Referral Summary*/
    protected String        referralReason = null;

    /**A list of Immunization*/
    protected Immunization[] immunizations = null;
    
    /**Family History*/
    protected String[] familyHistories = null;
    
    /**Social History*/
    protected String[] socialHistories = null;
    
    /**Surgical History*/
    protected String[] surgicalHistories = null;
    
    /**A list of Lab results */
    protected LabResult[] labResults = null;

	/**
	 * @return the referralReason
	 */
	public String getReferralReason() {
		return referralReason;
	}

	/**
	 * @param referralReason the referralReason to set
	 */
	public void setReferralReason(String referralReason) {
		this.referralReason = referralReason;
	}

	/**
	 * @return the immunizations
	 */
	public Immunization[] getImmunizations() {
		return immunizations;
	}

	/**
	 * @param immunizations the immunizations to set
	 */
	public void setImmunizations(Immunization[] immunizations) {
		this.immunizations = immunizations;
	}

	/**
	 * @return the familyHistories
	 */
	public String[] getFamilyHistories() {
		return familyHistories;
	}

	/**
	 * @param familyHistories the familyHistories to set
	 */
	public void setFamilyHistories(String[] familyHistories) {
		this.familyHistories = familyHistories;
	}

	/**
	 * @return the socialHistories
	 */
	public String[] getSocialHistories() {
		return socialHistories;
	}

	/**
	 * @param socialHistories the socialHistories to set
	 */
	public void setSocialHistories(String[] socialHistories) {
		this.socialHistories = socialHistories;
	}

	/**
	 * @return the surgicalHistories
	 */
	public String[] getSurgicalHistories() {
		return surgicalHistories;
	}

	/**
	 * @param surgicalHistories the surgicalHistories to set
	 */
	public void setSurgicalHistories(String[] surgicalHistories) {
		this.surgicalHistories = surgicalHistories;
	}

	/**
	 * @return the labResults
	 */
	public LabResult[] getLabResults() {
		return labResults;
	}

	/**
	 * @param labResults the labResults to set
	 */
	public void setLabResults(LabResult[] labResults) {
		this.labResults = labResults;
	}


}
