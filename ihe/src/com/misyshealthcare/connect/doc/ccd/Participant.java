package com.misyshealthcare.connect.doc.ccd;

import com.misyshealthcare.connect.base.demographicdata.Address;
import com.misyshealthcare.connect.base.demographicdata.PhoneNumber;
import com.misyshealthcare.connect.util.LibraryConfig;
import com.misyshealthcare.connect.util.OID;

/**
 * This class represents a Participant such as authenticator, legalAuthenticator and Performer. This
 * object is used to construct document.
 *
 * @author Wenzhi Li
 * @version 2.0, Oct 17, 2005
 */
public class Participant {

    protected Id id;
    protected PersonName personName;
    protected Address[] address;
    protected PhoneNumber[] phoneNumbers;
	/**
	 * @return the id
	 */
	public Id getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(Id id) {
		this.id = id;
	}
	/**
	 * @return the personName
	 */
	public PersonName getPersonName() {
		return personName;
	}
	/**
	 * @param personName the personName to set
	 */
	public void setPersonName(PersonName personName) {
		this.personName = personName;
	}
	/**
	 * @return the address
	 */
	public Address[] getAddress() {
		return address;
	}
	/**
	 * @param address the address to set
	 */
	public void setAddress(Address[] address) {
		this.address = address;
	}
	/**
	 * @return the phoneNumbers
	 */
	public PhoneNumber[] getPhoneNumbers() {
		return phoneNumbers;
	}
	/**
	 * @param phoneNumbers the phoneNumbers to set
	 */
	public void setPhoneNumbers(PhoneNumber[] phoneNumbers) {
		this.phoneNumbers = phoneNumbers;
	}




}


