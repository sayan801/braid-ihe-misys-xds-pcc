package com.misyshealthcare.connect.doc.ccd;

import java.util.Calendar;

/**
 *  
 *
 * @author Wenzhi Li
 * @version 3.0, Dec 6, 2007
 */
public class Author {
    private Participant authorPerson = null;
    private String[] authorRoles = null;
    private String[] authorSpecialities = null;
    private Calendar startParticipationTime = null;
    private Organization organization = null;
    private AuthorDevice authorDevice = null;
	/**
	 * @return the authorPerson
	 */
	public Participant getAuthorPerson() {
		return authorPerson;
	}
	/**
	 * @param authorPerson the authorPerson to set
	 */
	public void setAuthorPerson(Participant authorPerson) {
		this.authorPerson = authorPerson;
	}
	/**
	 * @return the authorRoles
	 */
	public String[] getAuthorRoles() {
		return authorRoles;
	}
	/**
	 * @param authorRoles the authorRoles to set
	 */
	public void setAuthorRoles(String[] authorRoles) {
		this.authorRoles = authorRoles;
	}
	/**
	 * @return the authorSpecialities
	 */
	public String[] getAuthorSpecialities() {
		return authorSpecialities;
	}
	/**
	 * @param authorSpecialities the authorSpecialities to set
	 */
	public void setAuthorSpecialities(String[] authorSpecialities) {
		this.authorSpecialities = authorSpecialities;
	}
	/**
	 * @return the startParticipationTime
	 */
	public Calendar getStartParticipationTime() {
		return startParticipationTime;
	}
	/**
	 * @param startParticipationTime the startParticipationTime to set
	 */
	public void setStartParticipationTime(Calendar startParticipationTime) {
		this.startParticipationTime = startParticipationTime;
	}
	/**
	 * @return the organization
	 */
	public Organization getOrganization() {
		return organization;
	}
	/**
	 * @param organization the organization to set
	 */
	public void setOrganization(Organization organization) {
		this.organization = organization;
	}
	/**
	 * @return the authorDevice
	 */
	public AuthorDevice getAuthorDevice() {
		return authorDevice;
	}
	/**
	 * @param authorDevice the authorDevice to set
	 */
	public void setAuthorDevice(AuthorDevice authorDevice) {
		this.authorDevice = authorDevice;
	}
    
    
}
