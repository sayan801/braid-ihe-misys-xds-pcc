package com.misyshealthcare.connect.doc.ccd;

import com.misyshealthcare.connect.base.clinicaldata.AdvanceDirective;
import com.misyshealthcare.connect.base.clinicaldata.Allergy;
import com.misyshealthcare.connect.base.clinicaldata.ClinicalNotes;
import com.misyshealthcare.connect.base.clinicaldata.Measurements;
import com.misyshealthcare.connect.base.clinicaldata.Medication;
import com.misyshealthcare.connect.base.clinicaldata.Procedure;
import com.misyshealthcare.connect.base.clinicaldata.SimpleProblem;
import com.misyshealthcare.connect.base.demographicdata.Insurance;

/**
 *
 * @author Wenzhi Li
 * @version 2.0, Nov 24, 2006
 */
public abstract class XDSMSData extends CCDData {
    /**History of present Illness*/
    protected String[]        historyOfPresentIllness = null;

    /**A list of patient's problems, used in Condition component*/
    protected SimpleProblem[] activeProblems = null;
    
    /**A list of patient's problems, used in Condition component*/
    protected SimpleProblem[] resolvedProblems = null;
    
    /**A list of patient's allergies, used in Allergy component*/
    protected Allergy[] allergies = null;
    
    /**A list of patient's current medications.*/
    protected Medication[] medications = null;
      
    /**Physical Exam*/
    protected String[] physicalExams = null;
    
    /**Review of Systems*/
    protected String[] reviewOfSystems = null;
   
    /**Plan of Care*/
    protected String[] planOfCares = null;
   
    /**Vital Signs*/
    protected Measurements[] measurements = null;
   
    /**A list of Advance Directives */
    protected AdvanceDirective[] advanceDirectives = null;

    /**A list of IProcedures */
    protected Procedure[] procedures = null;
    
    /**Insurance*/
    protected Insurance[] insurances = null;

	/**
	 * @return the historyOfPresentIllness
	 */
	public String[] getHistoryOfPresentIllness() {
		return historyOfPresentIllness;
	}

	/**
	 * @param historyOfPresentIllness the historyOfPresentIllness to set
	 */
	public void setHistoryOfPresentIllness(String[] historyOfPresentIllness) {
		this.historyOfPresentIllness = historyOfPresentIllness;
	}

	/**
	 * @return the activeProblems
	 */
	public SimpleProblem[] getActiveProblems() {
		return activeProblems;
	}

	/**
	 * @param activeProblems the activeProblems to set
	 */
	public void setActiveProblems(SimpleProblem[] activeProblems) {
		this.activeProblems = activeProblems;
	}

	/**
	 * @return the resolvedProblems
	 */
	public SimpleProblem[] getResolvedProblems() {
		return resolvedProblems;
	}

	/**
	 * @param resolvedProblems the resolvedProblems to set
	 */
	public void setResolvedProblems(SimpleProblem[] resolvedProblems) {
		this.resolvedProblems = resolvedProblems;
	}

	/**
	 * @return the allergies
	 */
	public Allergy[] getAllergies() {
		return allergies;
	}

	/**
	 * @param allergies the allergies to set
	 */
	public void setAllergies(Allergy[] allergies) {
		this.allergies = allergies;
	}

	/**
	 * @return the medications
	 */
	public Medication[] getMedications() {
		return medications;
	}

	/**
	 * @param medications the medications to set
	 */
	public void setMedications(Medication[] medications) {
		this.medications = medications;
	}

	/**
	 * @return the physicalExams
	 */
	public String[] getPhysicalExams() {
		return physicalExams;
	}

	/**
	 * @param physicalExams the physicalExams to set
	 */
	public void setPhysicalExams(String[] physicalExams) {
		this.physicalExams = physicalExams;
	}

	/**
	 * @return the reviewOfSystems
	 */
	public String[] getReviewOfSystems() {
		return reviewOfSystems;
	}

	/**
	 * @param reviewOfSystems the reviewOfSystems to set
	 */
	public void setReviewOfSystems(String[] reviewOfSystems) {
		this.reviewOfSystems = reviewOfSystems;
	}

	/**
	 * @return the planOfCares
	 */
	public String[] getPlanOfCares() {
		return planOfCares;
	}

	/**
	 * @param planOfCares the planOfCares to set
	 */
	public void setPlanOfCares(String[] planOfCares) {
		this.planOfCares = planOfCares;
	}

	/**
	 * @return the measurements
	 */
	public Measurements[] getMeasurements() {
		return measurements;
	}

	/**
	 * @param measurements the measurements to set
	 */
	public void setMeasurements(Measurements[] measurements) {
		this.measurements = measurements;
	}

	/**
	 * @return the advanceDirectives
	 */
	public AdvanceDirective[] getAdvanceDirectives() {
		return advanceDirectives;
	}

	/**
	 * @param advanceDirectives the advanceDirectives to set
	 */
	public void setAdvanceDirectives(AdvanceDirective[] advanceDirectives) {
		this.advanceDirectives = advanceDirectives;
	}

	/**
	 * @return the procedures
	 */
	public Procedure[] getProcedures() {
		return procedures;
	}

	/**
	 * @param procedures the procedures to set
	 */
	public void setProcedures(Procedure[] procedures) {
		this.procedures = procedures;
	}

	/**
	 * @return the insurances
	 */
	public Insurance[] getInsurances() {
		return insurances;
	}

	/**
	 * @param insurances the insurances to set
	 */
	public void setInsurances(Insurance[] insurances) {
		this.insurances = insurances;
	}
    
    

}
