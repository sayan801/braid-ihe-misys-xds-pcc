package com.misyshealthcare.connect.doc.ccd;

import com.misyshealthcare.connect.base.SharedEnums.PatientContactType;

/**
 *  
 *
 * @author Wenzhi Li
 * @version 3.0, Dec 6, 2007
 */
public class PatientContact {
	private Participant participant = null;
	private PatientContactType patientContactType = null;			
	private CodeSystem personalRelationshipRoleType = null;
	private EffectiveTime effectiveTime = null;
	/**
	 * @return the participant
	 */
	public Participant getParticipant() {
		return participant;
	}
	/**
	 * @param participant the participant to set
	 */
	public void setParticipant(Participant participant) {
		this.participant = participant;
	}
	/**
	 * @return the patientContactType
	 */
	public PatientContactType getPatientContactType() {
		return patientContactType;
	}
	/**
	 * @param patientContactType the patientContactType to set
	 */
	public void setPatientContactType(PatientContactType patientContactType) {
		this.patientContactType = patientContactType;
	}
	/**
	 * @return the personalRelationshipRoleType
	 */
	public CodeSystem getPersonalRelationshipRoleType() {
		return personalRelationshipRoleType;
	}
	/**
	 * @param personalRelationshipRoleType the personalRelationshipRoleType to set
	 */
	public void setPersonalRelationshipRoleType(
			CodeSystem personalRelationshipRoleType) {
		this.personalRelationshipRoleType = personalRelationshipRoleType;
	}
	/**
	 * @return the effectiveTime
	 */
	public EffectiveTime getEffectiveTime() {
		return effectiveTime;
	}
	/**
	 * @param effectiveTime the effectiveTime to set
	 */
	public void setEffectiveTime(EffectiveTime effectiveTime) {
		this.effectiveTime = effectiveTime;
	}
	
}
