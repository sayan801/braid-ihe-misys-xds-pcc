package com.misyshealthcare.connect.base;

import java.io.InputStream;

/**
 * This interface defines functions for accessing the
 * content bytes of a document.
 * 
 * @author Jim Firby
 * @version 2.0 - Dec 12, 2005
 */
public interface IDocumentContents {

	/**
	 * Get the contents of the supplied document as a stream
	 * of bytes.
	 * 
	 * @return The contents as a stream of bytes
	 */
	public InputStream getDocumentContentStream() throws DocumentException;
	
}
