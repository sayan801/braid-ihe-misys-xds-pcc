package com.misyshealthcare.connect.base;

import com.misyshealthcare.connect.net.Identifier;

/**
 * This class is a container class for a Patient ID, which contains both id number
 * and full data of assigning authority.
 *
 * @author Wenzhi Li
 * @version 2.1, Mar 21, 2007
 */
public class PatientIdentifier {
    /*The number part of the patient ID */
    private String idNumber;
    /*The assignin authority part of the patient ID */
    private Identifier identifier;

    public String getIdNumber() {
        return idNumber;
    }

    public void setIdNumber(String id) {
        this.idNumber = id;
    }

    public Identifier getIdentifier() {
        return identifier;
    }

    public void setIdentifier(Identifier identifier) {
       this.identifier = identifier;
    }
}
